def cargar_yaml(path: Path) -> dict[str, Any]:
    """Load YAML file."""
    if not path.exists():
        raise FileNotFoundError(f"No existe el archivo requerido: {path}")

    with open(path, "r", encoding="utf-8") as file:
        data = yaml.safe_load(file)

    if data is None:
        return {}

    if not isinstance(data, dict):
        raise ValueError(f"El archivo YAML no contiene un diccionario válido: {path}")

    return data


def resolver_ruta_proyecto(path_value: str | Path) -> Path:
    """
    Resolve a path from config.

    If the path is absolute, keep it.
    If it is relative, resolve it from PROJECT_ROOT.
    """
    path = Path(path_value)

    if path.is_absolute():
        return path

    return PROJECT_ROOT / path


def abrir_conexion_intermedia() -> sqlite3.Connection:
    """Open SQLite database generated by Module 3B."""
    if not INTERIM_DB_PATH.exists():
        raise FileNotFoundError(
            f"No existe la base intermedia: {INTERIM_DB_PATH}. "
            "Ejecute primero el Módulo 3B para generar la tabla grupos_xy."
        )

    conn = sqlite3.connect(INTERIM_DB_PATH)
    conn.execute("PRAGMA temp_store = MEMORY;")
    conn.execute("PRAGMA cache_size = -200000;")
    return conn


def tabla_existe(conn: sqlite3.Connection, table_name: str) -> bool:
    """Check if a SQLite table exists."""
    sql = """
    SELECT COUNT(*) AS n
    FROM sqlite_master
    WHERE type = 'table' AND name = ?;
    """
    n = conn.execute(sql, (table_name,)).fetchone()[0]
    return int(n) > 0


def normalizar_coord(series: pd.Series) -> pd.Series:
    """
    Convert coordinates to numeric values and round them consistently.

    This reduces failed joins caused by tiny floating point representation
    differences between the source GeoPackage and the intermediate SQLite DB.
    """
    return pd.to_numeric(series, errors="coerce").round(COORD_DECIMALS)


def cargar_grupos_xy() -> pd.DataFrame:
    """Load XY groups from the intermediate SQLite database."""
    conn = abrir_conexion_intermedia()

    try:
        if not tabla_existe(conn, "grupos_xy"):
            raise ValueError(
                "No existe la tabla grupos_xy en la base intermedia. "
                "Ejecute primero el Módulo 3B."
            )

        sql = """
        SELECT
            xy_group_id,
            lon,
            lat,
            n_registros,
            n_paises,
            n_fuentes,
            n_anios,
            n_nivel1,
            n_nivel2,
            anio_min,
            anio_max,
            pais_grupo,
            conf_integrada_promedio,
            conf_integrada_min,
            conf_integrada_max,
            tipo_grupo_xy
        FROM grupos_xy;
        """

        grupos_xy = pd.read_sql_query(sql, conn)

    finally:
        conn.close()

    required = {"lon", "lat"}
    missing = required.difference(grupos_xy.columns)
    if missing:
        raise ValueError(
            "La tabla grupos_xy no contiene los campos requeridos: "
            f"{sorted(missing)}"
        )

    grupos_xy["_lon_join"] = normalizar_coord(grupos_xy["lon"])
    grupos_xy["_lat_join"] = normalizar_coord(grupos_xy["lat"])

    grupos_xy = grupos_xy.drop(columns=["lon", "lat"])

    duplicated = grupos_xy.duplicated(subset=["_lon_join", "_lat_join"]).sum()
    if duplicated:
        print(
            "Advertencia: grupos_xy contiene coordenadas duplicadas después de "
            f"normalizar lon/lat: {duplicated:,} duplicados."
        )

    return grupos_xy


def limpiar_nombre_archivo(value: object) -> str:
    """Create a safe text fragment for filenames."""
    if pd.isna(value):
        return "NA"

    text = str(value)
    text = text.strip()
    text = text.replace(" ", "_")
    text = re.sub(r"[^A-Za-z0-9_\-]+", "", text)
    text = re.sub(r"_+", "_", text)

    if not text:
        return "NA"

    return text[:80]


def exportar_tabla_conteo(
    df: pd.DataFrame,
    field: str | None,
    output_csv: Path,
) -> None:
    """Export a frequency table for a field."""
    output_csv.parent.mkdir(parents=True, exist_ok=True)

    if not field or field not in df.columns:
        pd.DataFrame(
            [{"warning": f"Campo no encontrado o no configurado: {field}"}]
        ).to_csv(output_csv, index=False, encoding="utf-8-sig")
        return

    tabla = (
        df.groupby(field, dropna=False)
        .size()
        .reset_index(name="records")
        .sort_values("records", ascending=False)
    )

    total = tabla["records"].sum()
    if total > 0:
        tabla["percentage"] = (tabla["records"] / total * 100).round(4)
    else:
        tabla["percentage"] = 0.0

    tabla.to_csv(output_csv, index=False, encoding="utf-8-sig")


def asegurar_columnas(
    df: pd.DataFrame,
    columns: list[str],
    fill_value: object = pd.NA,
) -> pd.DataFrame:
    """Ensure required columns exist to avoid downstream groupby/sort errors."""
    for col in columns:
        if col not in df.columns:
            df[col] = fill_value

    return df


def construir_unidades_extraccion(
    gdf: gpd.GeoDataFrame,
    year_field: str,
    country_field: str,
    source_field: str,
    level1_field: str,
    level2_field: str,
) -> pd.DataFrame:
    """
    Build unique extraction units by lon, lat and year.

    One extraction unit represents a unique combination:
    longitude + latitude + reference year.
    """
    required_keys = ["_lon_join", "_lat_join", year_field]
    missing_keys = [field for field in required_keys if field not in gdf.columns]

    if missing_keys:
        raise ValueError(
            "No se pueden construir unidades de extracción. "
            f"Faltan campos clave: {missing_keys}"
        )

    optional_fields = [
        "xy_group_id",
        "tipo_grupo_xy",
        "n_registros",
        "n_paises",
        "n_fuentes",
        "n_anios",
        "n_nivel1",
        "n_nivel2",
        "anio_min",
        "anio_max",
        "pais_grupo",
        country_field,
        source_field,
        level1_field,
        level2_field,
    ]

    gdf = asegurar_columnas(
        gdf.copy(),
        [country_field, source_field, level1_field, level2_field],
    )

    grouped = gdf.groupby(required_keys, dropna=False)

    units = grouped.size().reset_index(name="n_records_extract_unit")

    existing_first_fields = [f for f in optional_fields if f in gdf.columns]

    if existing_first_fields:
        first_values = grouped[existing_first_fields].first().reset_index()
        units = units.merge(first_values, on=required_keys, how="left")

    for label, field in {
        "country": country_field,
        "source": source_field,
        "level1": level1_field,
        "level2": level2_field,
    }.items():
        if field in gdf.columns:
            nunique = (
                grouped[field]
                .nunique(dropna=True)
                .reset_index(name=f"n_unique_{label}_extract_unit")
            )
            units = units.merge(nunique, on=required_keys, how="left")

    sort_cols = [
        col
        for col in [year_field, country_field, "_lon_join", "_lat_join"]
        if col in units.columns
    ]

    if sort_cols:
        units = units.sort_values(sort_cols, na_position="last").reset_index(drop=True)
    else:
        units = units.reset_index(drop=True)

    units.insert(
        0,
        "extract_id",
        [f"S2SR_{i:09d}" for i in range(1, len(units) + 1)],
    )

    units = units.rename(
        columns={
            "_lon_join": "lon",
            "_lat_join": "lat",
            year_field: "year_ref",
            country_field: "country",
            source_field: "source",
            level1_field: "level_1",
            level2_field: "level_2",
        }
    )

    units = asegurar_columnas(
        units,
        ["country", "source", "level_1", "level_2", "year_ref"],
    )

    return units


def exportar_batches(
    units: pd.DataFrame,
    batch_size: int,
) -> pd.DataFrame:
    """
    Export CSV batches for GEE.

    The batch size is only the maximum number of extraction units per CSV.
    It does not change the data logic.
    """
    if batch_size <= 0:
        raise ValueError("batch_size debe ser mayor que 0.")

    BATCHES_DIR.mkdir(parents=True, exist_ok=True)

    for old_csv in BATCHES_DIR.glob("*.csv"):
        old_csv.unlink()

    if units.empty:
        batch_index = pd.DataFrame(
            columns=["batch_id", "country", "year_ref", "records", "batch_csv"]
        )
        batch_index.to_csv(
            TABLES_DIR / "s2_sr_gee_batch_index.csv",
            index=False,
            encoding="utf-8-sig",
        )
        return batch_index

    units = asegurar_columnas(units.copy(), ["country", "year_ref"])

    batch_records = []
    group_fields = ["country", "year_ref"]

    for (country, year_ref), df_group in units.groupby(group_fields, dropna=False):
        df_group = df_group.sort_values(["extract_id"]).reset_index(drop=True)

        n_batches = (len(df_group) + batch_size - 1) // batch_size

        for batch_idx in range(n_batches):
            start = batch_idx * batch_size
            end = min(start + batch_size, len(df_group))

            df_batch = df_group.iloc[start:end].copy()

            country_name = limpiar_nombre_archivo(country)
            year_name = limpiar_nombre_archivo(year_ref)

            batch_name = (
                f"s2sr_units_{country_name}_{year_name}_"
                f"batch_{batch_idx + 1:03d}"
            )
            batch_csv = BATCHES_DIR / f"{batch_name}.csv"

            df_batch["batch_id"] = batch_name
            df_batch.to_csv(batch_csv, index=False, encoding="utf-8-sig")

            batch_records.append(
                {
                    "batch_id": batch_name,
                    "country": country,
                    "year_ref": year_ref,
                    "records": len(df_batch),
                    "batch_csv": str(batch_csv),
                }
            )

    batch_index = pd.DataFrame(batch_records)
    batch_index.to_csv(
        TABLES_DIR / "s2_sr_gee_batch_index.csv",
        index=False,
        encoding="utf-8-sig",
    )

    return batch_index


