def validar_config(config: dict[str, Any]) -> tuple[Path, str, dict[str, str]]:
    """Validate config and return input file, layer name and field mapping."""
    input_data = config.get("input_data", {})
    fields_cfg = config.get("fields", {})

    if not isinstance(input_data, dict):
        raise ValueError("La sección input_data de config/config.yaml no es válida.")

    if not isinstance(fields_cfg, dict):
        raise ValueError("La sección fields de config/config.yaml no es válida.")

    gpkg_rel = input_data.get("gpkg_file")
    table_name = input_data.get("gpkg_layer")

    if not gpkg_rel:
        raise ValueError("No se definió input_data.gpkg_file en config/config.yaml.")

    if not table_name:
        raise ValueError("No se definió input_data.gpkg_layer en config/config.yaml.")

    gpkg_path = resolver_ruta_proyecto(gpkg_rel)

    if not gpkg_path.exists():
        raise FileNotFoundError(
            "No existe el GeoPackage configurado en input_data.gpkg_file: "
            f"{gpkg_path}"
        )

    field_map = {
        "longitude": str(fields_cfg.get("longitude", "Longitud")),
        "latitude": str(fields_cfg.get("latitude", "Latitud")),
        "country": str(fields_cfg.get("country", "Pais_es")),
        "source": str(fields_cfg.get("source", "Fuente")),
        "year": str(fields_cfg.get("year", "Año")),
        "level_1": str(fields_cfg.get("level_1", "Nivel_1")),
        "level_2": str(fields_cfg.get("level_2", "Nivel_2")),
    }

    return gpkg_path, str(table_name), field_map


def validar_campos_requeridos(
    gdf: gpd.GeoDataFrame,
    required_fields: list[str],
) -> None:
    """Validate fields required in the input GeoPackage."""
    missing = [field for field in required_fields if field not in gdf.columns]

    if missing:
        available = sorted([str(col) for col in gdf.columns])
        raise ValueError(
            "Faltan campos requeridos en el GeoPackage original: "
            f"{missing}\n\nCampos disponibles:\n{available}"
        )


def preparar_gdf_elegible(
    gdf: gpd.GeoDataFrame,
    grupos_xy: pd.DataFrame,
    field_map: dict[str, str],
) -> tuple[gpd.GeoDataFrame, int]:
    """Filter records by year and join XY group information."""
    lon_field = field_map["longitude"]
    lat_field = field_map["latitude"]
    year_field = field_map["year"]

    validar_campos_requeridos(gdf, [lon_field, lat_field, year_field])

    gdf = gdf.copy()
    gdf[year_field] = pd.to_numeric(gdf[year_field], errors="coerce")

    gdf_eligible = gdf[
        (gdf[year_field] >= YEAR_MIN)
        & (gdf[year_field] <= YEAR_MAX)
    ].copy()

    gdf_eligible["_lon_join"] = normalizar_coord(gdf_eligible[lon_field])
    gdf_eligible["_lat_join"] = normalizar_coord(gdf_eligible[lat_field])

    # Avoid merge suffixes when rerunning over data that already has these columns.
    overlap_cols = [
        col
        for col in grupos_xy.columns
        if col in gdf_eligible.columns and col not in ["_lon_join", "_lat_join"]
    ]

    if overlap_cols:
        gdf_eligible = gdf_eligible.drop(columns=overlap_cols)

    gdf_eligible = gdf_eligible.merge(
        grupos_xy,
        on=["_lon_join", "_lat_join"],
        how="left",
    )

    unmatched_xy = int(gdf_eligible["xy_group_id"].isna().sum())

    return gdf_eligible, unmatched_xy


def exportar_resumenes(
    gdf_eligible: gpd.GeoDataFrame,
    units: pd.DataFrame,
    batch_index: pd.DataFrame,
    total_input: int,
    unmatched_xy: int,
    batch_size: int,
    field_map: dict[str, str],
) -> None:
    """Export summary CSV tables."""
    TABLES_DIR.mkdir(parents=True, exist_ok=True)

    year_field = field_map["year"]
    country_field = field_map["country"]
    source_field = field_map["source"]
    level1_field = field_map["level_1"]
    level2_field = field_map["level_2"]

    summary = pd.DataFrame(
        [
            {
                "year_min": YEAR_MIN,
                "year_max": YEAR_MAX,
                "total_input_records": total_input,
                "eligible_records": len(gdf_eligible),
                "extract_units_lon_lat_year": len(units),
                "redundant_extractions_avoided": len(gdf_eligible) - len(units),
                "unmatched_xy_records": unmatched_xy,
                "batch_size": batch_size,
                "n_batches": len(batch_index),
                "output_gpkg": str(OUTPUT_GPKG),
                "output_layer": OUTPUT_LAYER,
                "extract_units_csv": str(EXTRACT_UNITS_CSV),
                "batches_dir": str(BATCHES_DIR),
            }
        ]
    )

    summary.to_csv(
        TABLES_DIR / "s2_sr_eligible_summary.csv",
        index=False,
        encoding="utf-8-sig",
    )

    exportar_tabla_conteo(
        gdf_eligible,
        country_field,
        TABLES_DIR / "s2_sr_eligible_by_country.csv",
    )
    exportar_tabla_conteo(
        gdf_eligible,
        year_field,
        TABLES_DIR / "s2_sr_eligible_by_year.csv",
    )
    exportar_tabla_conteo(
        gdf_eligible,
        source_field,
        TABLES_DIR / "s2_sr_eligible_by_source.csv",
    )
    exportar_tabla_conteo(
        gdf_eligible,
        level1_field,
        TABLES_DIR / "s2_sr_eligible_by_level1.csv",
    )
    exportar_tabla_conteo(
        gdf_eligible,
        level2_field,
        TABLES_DIR / "s2_sr_eligible_by_level2.csv",
    )
    exportar_tabla_conteo(
        gdf_eligible,
        "tipo_grupo_xy",
        TABLES_DIR / "s2_sr_eligible_by_tipo_grupo_xy.csv",
    )

    units = asegurar_columnas(units.copy(), ["country", "year_ref"])

    units_by_country_year = (
        units.groupby(["country", "year_ref"], dropna=False)
        .size()
        .reset_index(name="extract_units")
        .sort_values(["country", "year_ref"], na_position="last")
    )

    units_by_country_year.to_csv(
        TABLES_DIR / "s2_sr_extract_units_by_country_year.csv",
        index=False,
        encoding="utf-8-sig",
    )


def exportar_gpkg(gdf_eligible: gpd.GeoDataFrame) -> None:
    """Export eligible records to GeoPackage."""
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    drop_cols = [col for col in ["_lon_join", "_lat_join"] if col in gdf_eligible.columns]
    export_gdf = gdf_eligible.drop(columns=drop_cols)

    if OUTPUT_GPKG.exists():
        OUTPUT_GPKG.unlink()

    export_gdf.to_file(
        OUTPUT_GPKG,
        layer=OUTPUT_LAYER,
        driver="GPKG",
    )


def parse_args() -> argparse.Namespace:
    """Parse command line arguments."""
    parser = argparse.ArgumentParser(
        description="Prepara puntos 2018-2022 para extracción Sentinel-2 SR en GEE."
    )

    parser.add_argument(
        "--batch-size",
        type=int,
        default=DEFAULT_BATCH_SIZE,
        help=(
            "Número máximo de unidades Longitud-Latitud-Año por CSV batch. "
            f"Valor por defecto: {DEFAULT_BATCH_SIZE}."
        ),
    )

    parser.add_argument(
        "--skip-gpkg",
        action="store_true",
        help="No exporta el GeoPackage completo de registros elegibles.",
    )

    return parser.parse_args()


def main() -> None:
    args = parse_args()

    crear_carpetas_salida()

    if args.batch_size <= 0:
        raise ValueError("--batch-size debe ser mayor que 0.")

    config = cargar_yaml(CONFIG_PATH)
    gpkg_path, table_name, field_map = validar_config(config)

    lon_field = field_map["longitude"]
    lat_field = field_map["latitude"]
    year_field = field_map["year"]
    country_field = field_map["country"]
    source_field = field_map["source"]
    level1_field = field_map["level_1"]
    level2_field = field_map["level_2"]

    print("============================================================")
    print("PGBM - Preparación de puntos para GEE Sentinel-2 SR")
    print("============================================================")
    print(f"Project root: {PROJECT_ROOT}")
    print(f"Config: {CONFIG_PATH}")
    print(f"Base intermedia: {INTERIM_DB_PATH}")
    print(f"GeoPackage: {gpkg_path}")
    print(f"Capa: {table_name}")
    print(f"Periodo: {YEAR_MIN}-{YEAR_MAX}")
    print(f"Campo año: {year_field}")
    print(f"Campo longitud: {lon_field}")
    print(f"Campo latitud: {lat_field}")
    print(f"Campo país: {country_field}")
    print(f"Campo fuente: {source_field}")
    print(f"Campo nivel 1: {level1_field}")
    print(f"Campo nivel 2: {level2_field}")
    print(f"Batch size: {args.batch_size:,}")

    registrar_log("Inicio de preparación de puntos para GEE Sentinel-2 SR.")

    print("\nCargando grupos_xy del Módulo 3B...")
    grupos_xy = cargar_grupos_xy()
    print(f"Grupos XY cargados: {len(grupos_xy):,}")

    print("\nLeyendo GeoPackage original...")
    gdf = gpd.read_file(gpkg_path, layer=table_name)
    total_input = len(gdf)
    print(f"Total registros entrada: {total_input:,}")

    print(f"\nFiltrando registros elegibles {YEAR_MIN}-{YEAR_MAX}...")
    gdf_eligible, unmatched_xy = preparar_gdf_elegible(
        gdf=gdf,
        grupos_xy=grupos_xy,
        field_map=field_map,
    )

    print(f"Registros elegibles {YEAR_MIN}-{YEAR_MAX}: {len(gdf_eligible):,}")
    print(f"Registros elegibles sin unión con grupos_xy: {unmatched_xy:,}")

    print("\nConstruyendo unidades únicas Longitud-Latitud-Año para GEE...")
    units = construir_unidades_extraccion(
        gdf=gdf_eligible,
        year_field=year_field,
        country_field=country_field,
        source_field=source_field,
        level1_field=level1_field,
        level2_field=level2_field,
    )

    print(f"Unidades de extracción GEE: {len(units):,}")

    units.to_csv(EXTRACT_UNITS_CSV, index=False, encoding="utf-8-sig")
    print(f"CSV unidades GEE: {EXTRACT_UNITS_CSV}")

    extract_lookup = units[
        ["extract_id", "lon", "lat", "year_ref", "n_records_extract_unit"]
    ].rename(
        columns={
            "lon": "_lon_join",
            "lat": "_lat_join",
            "year_ref": year_field,
        }
    )

    overlap_cols = [
        col
        for col in ["extract_id", "n_records_extract_unit"]
        if col in gdf_eligible.columns
    ]

    if overlap_cols:
        gdf_eligible = gdf_eligible.drop(columns=overlap_cols)

    gdf_eligible = gdf_eligible.merge(
        extract_lookup,
        on=["_lon_join", "_lat_join", year_field],
        how="left",
    )

    missing_extract_id = int(gdf_eligible["extract_id"].isna().sum())
    if missing_extract_id:
        print(
            "Advertencia: algunos registros elegibles no recibieron extract_id: "
            f"{missing_extract_id:,}"
        )

    print("\nExportando batches CSV para GEE...")
    batch_index = exportar_batches(units, batch_size=args.batch_size)
    print(f"Batches generados: {len(batch_index):,}")

    print("\nExportando tablas resumen...")
    exportar_resumenes(
        gdf_eligible=gdf_eligible,
        units=units,
        batch_index=batch_index,
        total_input=total_input,
        unmatched_xy=unmatched_xy,
        batch_size=args.batch_size,
        field_map=field_map,
    )

    if not args.skip_gpkg:
        print("\nExportando GeoPackage de registros elegibles...")
        exportar_gpkg(gdf_eligible)
        print(f"GeoPackage exportado: {OUTPUT_GPKG}")
    else:
        print("\nSe omitió la exportación del GeoPackage por --skip-gpkg.")

    print("\nGenerando reporte...")
    generar_reporte(
        total_input=total_input,
        total_eligible=len(gdf_eligible),
        total_units=len(units),
        total_batches=len(batch_index),
        unmatched_xy=unmatched_xy,
        batch_size=args.batch_size,
    )

    registrar_log(
        "Preparación de puntos para GEE Sentinel-2 SR ejecutada correctamente. "
        f"Registros elegibles: {len(gdf_eligible)}. "
        f"Unidades GEE: {len(units)}. "
        f"Batches: {len(batch_index)}."
    )

    print("\nDone.")
    print(f"Registros elegibles {YEAR_MIN}-{YEAR_MAX}: {len(gdf_eligible):,}")
    print(f"Unidades únicas Longitud-Latitud-Año: {len(units):,}")
    print(f"Extracciones redundantes evitadas: {len(gdf_eligible) - len(units):,}")
    print(f"Batches generados: {len(batch_index):,}")
    print(f"CSV unidades GEE: {EXTRACT_UNITS_CSV}")
    print(f"GeoPackage: {OUTPUT_GPKG}")
    print(f"Reporte: {REPORTS_DIR / '06_s2_sr_gee_input.md'}")


if __name__ == "__main__":
    try:
        main()
    except Exception as exc:
        registrar_log(
            "Error durante la preparación de puntos para GEE Sentinel-2 SR. "
            f"{type(exc).__name__}: {exc}"
        )
        traceback.print_exc()
        raise
