def find_project_root() -> Path:
    """
    Find project root by searching upward for config/config.yaml.

    This makes the script robust when executed from bash, VS Code, or another
    working directory, as long as the file is inside the repository.
    """
    candidates = []

    try:
        candidates.append(Path(__file__).resolve().parent)
    except NameError:
        pass

    candidates.append(Path.cwd().resolve())

    visited: set[Path] = set()

    for start in candidates:
        for path in [start, *start.parents]:
            if path in visited:
                continue
            visited.add(path)

            if (path / "config" / "config.yaml").exists():
                return path

    raise FileNotFoundError(
        "No se pudo localizar la raíz del proyecto. "
        "El script debe estar dentro del repositorio y debe existir config/config.yaml."
    )


